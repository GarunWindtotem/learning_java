package com.example.basics;

public class f_constants {
    // main
    public static void main(String[] args) {
        // variable
        float pi = 3.14F;
        System.out.println("pi = " + pi);
        pi = 1;
        System.out.println("pi = " + pi);

        // constant
        final float PI = 3.14F;
        System.out.println("PI = " + PI);
    }
}
