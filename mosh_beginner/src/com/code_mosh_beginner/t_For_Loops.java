package com.code_mosh_beginner;

public class t_For_Loops {

    public static void main(String[] args) {
        //   init     condition increment
        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello " + i);
        }

        System.out.println("");

        for (int i = 5; i > 0; i--) {
            System.out.println("Hello " + i);
        }
    }
}
